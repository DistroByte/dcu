#!/usr/bin/env python3

x = input()
n = len(x)
i = 0

while i < n:
    if x[i] == "a":
        print("a")
        i = n
    elif x[i] == "b":
        print("b")
        i = n
    elif x[i] == "c":
        print("c")
        i = n
    elif x[i] == "d":
        print("d")
        i = n
    elif x[i] == "e":
        print("e")
        i = n
    elif x[i] == "f":
        print("f")
        i = n
    elif x[i] == "g":
        print("g")
        i = n
    i += 1
